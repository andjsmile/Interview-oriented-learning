/**
*
* 作为模型的student对象
*/

public class student{
    private String rollNo;
    private String name;

    public String getRollNo(){
        return rollNo;
    }

    public String getName(){
        return name;
    }

    public void setRollNo(String rollNo){
        this.rollNo=rollNo;
    }

    public void setName(String name){
        this.name=name;
    }
}